
public class _05CharTest {
	/***
	 * 字符类型，char 一个符号就是字符；
	 * @param args
	 */
	public static void main(String[] args) {
		System.out.println("字符串类型，String");
		
		char c = 'A';
		System.out.println("char类型数据："+c);
		
		char c1 = 65;//表示字符的另外一种形式；
		System.out.println(c1);
		
		char c2 = '\'';//要想显示某个符号就需要使用转义符（ 斜杠➕符号，如：\'）
		System.out.println(c2);
		
		
	}

}
